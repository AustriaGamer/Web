let number;
number = Math.round(Math.random() * 5) + 5;
switch (number) {
  case 10:
    document.write("Ten");
    break;
  case 9:
    document.write("Nine");
    break;
  case 8:
    document.write("Eight");
    break;
  case 7:
    document.write("Seven");
    break;
  case 6:
    document.write("Six");
    break;
  case 5:
    document.write("Five");
    break;
}
